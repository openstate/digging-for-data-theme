          <h1>Digitale Indiana Jones gezocht! Doe mee met de app challenge</h1>
          <h4>De provincie Zuid-Holland is een rijk archeologisch gebied. Archeologie vertelt veel over onze geschiedenis. De provincie Zuid-Holland wil samen met anderen erfgoed helpen ontdekken, beschermen en zorgen dat iedereen er kennis mee kan maken. Archeologie is daarvan een mooi voorbeeld. <br><br>

          De provincie Zuid-Holland stelt haar archeologische objecten, zoals Romeinse munten, 3D schalen en opgravingsdocumentatie, beschikbaar als open data. Hiermee zijn duizenden objecten, afbeeldingen en documenten beschikbaar voor hergebruik. De provincie Zuid-Holland daagt jou daarom uit om deze digitale schatkamer te hergebruiken tijdens de Digging for Data app challenge. Het winnende idee ontvangt van de provincie Zuid-Holland €20.000 ontwikkelbudget om de app daadwerkelijk te kunnen ontwikkelen en bouwen! <br><br>

          Tijdens de Digging for Data app challenge worden ontwikkelaars uitgenodigd om een uitgewerkt app-idee in te sturen. Het idee moet als uitgangspunt hebben om het publieksbereik van archeologische vondsten te vergroten. Archeologie moet fascineren, inspireren en de applicatie moet natuurlijk ook een educatieve meerwaarde hebben door gebruikers van de app kennis te laten maken met de unieke archeologische stukken van de provincie Zuid-Holland. <br><br>

          Heb jij affiniteit met zowel archeologie als datawetenschap en informatica? Heb jij een fantastisch idee voor een archeologie applicatie en kan jij het ook daadwerkelijk uitvoeren? Kortom, ben jij Indiana Jones 2.0 en wil jij met jouw ‘ontdekking’ kans maken op €20.000 ontwikkelbudget? Kom dan 20 april 2018 naar de kick-off meeting bij het COMM Museum in Den Haag en kom alles te weten over de app challenge. </h4>
